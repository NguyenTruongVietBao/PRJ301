    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        request.setCharacterEncoding("UTF-8");
        String userID_raw = request.getParameter("id");
        String fullName_raw = request.getParameter("name");
        String password_raw = request.getParameter("pass");
        String roleID_raw = request.getParameter("role");
        boolean status_raw = true;
        try {
            userDAO uD = new userDAO();
            user u = uD.getUserID(userID_raw);
            if(u != null){
                String ms ="id existed";
                request.setAttribute("error", ms);
                request.getRequestDispatcher("add.jsp").forward(request, response);
            }else{
                user u1 = new user(userID_raw, fullName_raw, password_raw, roleID_raw, status_raw);
                uD.insertServlet(u1);
                response.sendRedirect("userServlet");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

